package coe528.project;

import java.io.IOException;
import javafx.event.ActionEvent;
import javafx.fxml.FXML;
import javafx.fxml.FXMLLoader;
import javafx.scene.Node;
import javafx.scene.Parent;
import javafx.scene.Scene;
import javafx.stage.Stage;

/**
 *
 * @author Paresh
 */
public class MainController {
    
    private Stage stage;
    private Scene scene;
    private Parent root;
    
    @FXML
    private void handleManagerLoginAction(ActionEvent event) throws IOException {
        root = FXMLLoader.load(getClass().getResource("ManagerLoginScene.fxml"));
        stage = (Stage)((Node)event.getSource()).getScene().getWindow();
        scene = new Scene(root);
        stage.setScene(scene);
        stage.show();
    }
    
    @FXML
    private void handleCustomerLoginAction(ActionEvent event) throws IOException {
        root = FXMLLoader.load(getClass().getResource("CustomerLoginScene.fxml"));
        stage = (Stage)((Node)event.getSource()).getScene().getWindow();
        scene = new Scene(root);
        stage.setScene(scene);
        stage.show();
    }
}
